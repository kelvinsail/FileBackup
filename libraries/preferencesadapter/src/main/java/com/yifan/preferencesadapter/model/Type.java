package com.yifan.preferencesadapter.model;

/**
 * 设置项类型枚举类
 *
 * Created by yifan on 2016/12/22.
 */
public enum Type {

    /**
     * 普通点击设置项
     */
    normal,
    /**
     * 设置集合项
     */
    group,
    /**
     * 带单选的设置项
     */
    checkGroup,
    /**
     * 带有开关的设置项
     */
    switchable;

}
